    private void btnSelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser(System.getProperty("user.home"));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        fileChooser.setFileFilter(filter);
        fileChooser.setMultiSelectionEnabled(false);
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File f = fileChooser.getSelectedFile();
            try {
                if (!FileProcessor.getResourcesFromFile(f)) {
                    JOptionPane.showMessageDialog(rootPane, "Selected file doesn't seem to contain any resources.", "File Error!!!", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                Object[] resources = FileProcessor.getResources().toArray();
                cmbResources.setModel(new DefaultComboBoxModel(resources));
            } catch (IOException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Error selecting file: " + ex.getMessage(), "File Error!!!", JOptionPane.ERROR_MESSAGE);
            }
        } else if (result == JFileChooser.ERROR_OPTION) {
            JOptionPane.showMessageDialog(rootPane, "Error selecting file.", "File Error!!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSelectFileActionPerformed

