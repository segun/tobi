    private void btnSaveToFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveToFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser(System.getProperty("user.home"));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("TEXT FILES", "txt", "text");
        fileChooser.setFileFilter(filter);
        fileChooser.setMultiSelectionEnabled(false);
        int result = fileChooser.showSaveDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File f = fileChooser.getSelectedFile();
            if(f.exists()) {
                int go = JOptionPane.showConfirmDialog(rootPane, "File " + f.getName() + " already exists. Do you want to overwrite?", "File Exists", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if(go == JOptionPane.CANCEL_OPTION) {
                    return;
                }
            }
            try (FileWriter writer = new FileWriter(f)) {
                writer.write(txtResult.getText());
                JOptionPane.showMessageDialog(rootPane, "Output written to file succesfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Error Occurred writing output file: " + e.getMessage(), "Error Writing File!!!", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, e);
            }
        } else if (result == JFileChooser.ERROR_OPTION) {
            JOptionPane.showMessageDialog(rootPane, "Error selecting file.", "File Error!!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveToFileActionPerformed

